# Exercise3: Create a dictionary of months with their numbers and print find the number for May.

# Create a dictionary
# Hint: dictionary = {key: value}
# Store the months as key and month number as value
months = {
    'January': 1,
    'February': 2,
    'March': 3,
    'April': 4,
    'May': 5,
    'June': 6,
    'July': 7,
    'August': 8,
    'September': 9,
    'October': 10,
    'November': 11,
    'December': 12
}

# Print the dictionary
print(months)

# Create variable with a month
month = 'May'

# Print the month number using the dictionary
print(months[month])